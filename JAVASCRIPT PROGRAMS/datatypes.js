//Number, String, Boolean, Undefined, Null BigInt, Symbol are Primitives
//Objects are non primitive data types
let age=24; //numeric data type
console.log(typeof age);
let name="John Doe";  //String Data Type
console.log(typeof name);
let isMarried=true;   //Boolean Data Type
console.log(typeof isMarried);
let x;               //Undefined Data Type
console.log(typeof x);
let nullVar=null;     //Null Data Type
console.log(typeof nullVar);
let salary=BigInt("1000000000000");    //Big Int Data Type
console.log(typeof salary)
let sym1 =Symbol("key");            //Symbol Data Type
console.log(typeof sym1);   //Symbol Data Type  (Optional description of symbol)